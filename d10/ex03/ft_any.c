int	ft_any(char **tab, int (*f)(char*))
{
	int n;
	int one;

	n = 0;
	one = 0;
	while (tab[n])
	{
		if (f(tab[n]) == 1)
			one = 1;
		n++;
	}
	if (one == 1)
		return (1);
	return (0);
}
