int	ft_count_if(char **tab, int (*f)(char*))
{
	int n;
	int one;

	n = 0;
	one = 0;
	while (tab[n])
	{
		if (f(tab[n]) == 1)
			one += 1;
		n++;
	}
	return (one);
}
