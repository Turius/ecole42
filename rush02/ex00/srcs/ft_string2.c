int		ft_strcmp_inv(char *s1, char *s2)
{
	int c;

	c = 0;
	while ((s1[c] == ' ' && s2[c] != ' ') || (s1[c] != ' ' && s2[c] == ' ') ||
			(s1[c] == '\n' && s2[c] == '\n'))
	{
		c++;
	}
	if (s1[c] == '\0' && s2[c] == '\0')
		return (0);
	else
		return (1);
}

int		ft_strcmp_shapes(char *s1, char *s2)
{
	int c;

	c = 0;
	while ((s1[c] == ' ' && s2[c] == ' ') || ((s1[c] != ' ' && s1[c] != '\n') ||
					(s2[c] != ' ' && s2[c] != '\n')) || (s1[c] == '\n' &&
						s2[c] == '\n'))
		c++;
	if (s1[c] == '\0' && s2[c] == '\0')
		return (0);
	else
		return (1);
}
