int		ft_atoi(char *str)
{
	int c;
	int n;
	int pos;

	pos = 1;
	n = 0;
	c = 0;
	while (str[c] == '\t' || str[c] == 32 ||
			str[c] == '\n' || str[c] == '\v' ||
			str[c] == '\f' || str[c] == '\r')
		c++;
	if (str[c] == 45)
		pos = -1;
	if (str[c] == 43 || str[c] == 45)
		c++;
	while (str[c] != '\0')
	{
		if (str[c] >= 48 && str[c] <= 57)
			n = n * 10 + str[c] - 48;
		else
			return (n * pos);
		c++;
	}
	return (n * pos);
}
